void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if (GPIO_Pin == CalBaselineButton_Pin){
		mode = CAL_BASELINE;
		showData();

	}
	else if (GPIO_Pin == CalAlarmButton_Pin){
		mode = CAL_ALARM_LEVEL;
		showData();

	}

}
